function axes2_CreateFcn(hObject, eventdata, handles)
% hObject    handle to axes2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    empty - handles not created until after all CreateFcns called

% Hint: place code in OpeningFcn to populate axes2

function update_map(handles)  
    handles.mapa_ruta = flipud(handles.mapa_ruta);
    [y_size, x_size] = size(handles.mapa_ruta);
    axes(handles.axes2)
    imagesc(handles.mapa_ruta);
    xlabel('X [mm]');
    ylabel('Y [mm]');
    title('Mapa');
    
    x = [0 100 200 300 400 500];
    y = [500 400 300 200 100 0];
    set(gca, 'XTick', linspace(0, x_size, 6));
    set(gca,'XTickLabel', cellstr(num2str(x(:))));
    set(gca, 'YTick', linspace(0, y_size, 6));
    set(gca,'YTickLabel', cellstr(num2str(y(:))));
